		<span>元璟资本的合伙人田敏称，</span>“共享员工”是典型的应付危机解决方法，但并不适用于所有企业。而且，“这个事过去以后，是不是大家还会持续的用这种模式，不好说。”她告诉投中网，很多业务有很强的季节性，这就需要采用灵活用工，不可能在波谷时还养着一波峰时期体量的人。
	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
		<span>能</span>“共享”，在于技能可被“标签化”
	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
		“共享员工”模式或不可复制。但此次疫情带来的思考可能还会围绕同一个问题：措手不及的风险来临时，企业的灵活性又如何凸显？员工多好，还是少好？
	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
		<span>针对此次疫情的用人情况，金柚网发布的《基于人力资源视角的疫情影响观察报告》认为，未来劳动力或将不再是被一纸劳动合同绑定为企业所有的一种生产要素，而是为企业灵活所用的一种经营资本。</span>
	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
		<span>而实际上，以承包商形式的灵活用工方式很早在互联网公司中流行。随着技术不断渗透和深入，越来越多不同形态的互联网公司出现。这些公司的商业模式、业务形态或岗位大部分能通过技术手段数字化，进行线上管理。</span>
	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
		58到家首席人才官段冬曾在《南风窗》的采访中表示，“过去公司是按职责来划分岗位，把人按照组织的职责放在不同的岗位上。但现在，所有的岗位是在围绕人的技能。人有什么技能，就能从事什么岗位，现在我们的管理是要把人打上‘标签’。”而这种“标签化”的模式，恰恰促进了零工经济的发展，也即是越来越多的自由职业者或斜杠青年出现。
	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
		<span>从这个层面理解，也能解释为什么在这次疫情里，餐饮行业线下门店首当其冲。服务行业由于激烈的竞争，服务质量成了关键。而服务质量强调人以及朝夕培训，所以线下门店会有大批长期雇佣的员工。因此，在此次疫情中，餐饮服务行业的优势也恰恰成了</span>“弱点”，因为服务质量暂时无法“标签化”。
	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
		<span>田敏告诉投中网，</span>“标签化”管理可能在原来的产业架构里面就不太可能实现，因为没有互联网的工具作为评判标准和监督管理手段。
	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
		<span>她向投中网举例一个典型的</span>“标签化”管理案例——微商。在微信生态下的灵活用工，销售人员变成了兼职，他们在线上通过朋友圈去推广，有效的补充了销售范围。
	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
		<span>这两年可以看到包括教育、化妆品、保健品等多品类品牌在采用这种方式，而且很多宝妈也因此成了微商。而管理宝妈的方式也很数据化，微信群统一管理，销售结果也能数据化，给予相应地提成。而这种数字化管理的方式，在这次疫情中也能看到很好地运用到其他领域的销售渠道商，如房地产公司通过上线自己的销售应用逐步</span>“微商化”。
	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
		<span>企业新挑战？</span>
	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
		<span>此次疫情，扩放大了人的技能</span>“标签化”——能被“共享”，多一个选择。疫情会改变零工经济吗？同样作为雇佣主，企业又将如何面对可能变化的“零工经济群体”。
	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
		<span>田敏不认为这次疫情会改变包括灵活用工在内的零工经济的趋势。因为零工经济早已存在目前在各个行业中。</span>
	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
		<span>她告诉投中网，这与中国劳动力结构发生改变有关。</span>“中国从原来的一个生产型的大国，到现在其实已经慢慢地就是变成消费服务型的个国家。原来的蓝领或者是知识结构层次稍微低的工人，逐渐地转向成为技能更高、知识层次稍微高的白领。我们在价格上会更缺稍微高端的工种。”
	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
		<span>陈鸿飞也表示，此次疫情中被大量关注的是生活服务行业，但实际上在零工经济里用得更多是有专业技能交付的岗位，中国的互联网公司里缺的是</span>“比如直播主播、设计人才、写手等。”
	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
		“从未来来看，创业公司和大公司肯定需要灵活用工作为一些职能部门的一些补充。”田敏说。不过，她也表示，公司平衡灵活用工与保持长期雇佣关系主要还是看公司商业模式、发展阶段以及业务属性。比如滴滴出行，早期的司机通过“众包”模式，但进入增长期为保障业务发展，也有雇用专职司机。
	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
		<span>刘军同样认为，一个公司处于什么发展阶段会有特定的用人需求，是否雇佣零工经济（外包），前提是谁更有效率以及出效果。</span>“如果内部可以完成，为什么要选择外部？如果内部不行，那么应该思考内部的机制是否不够创新，为什么达不到外部市场水平。”刘军说。
	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
