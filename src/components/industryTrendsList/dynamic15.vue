	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
		<span>那么，此次疫情会为不同行业的企业带来怎么样的用工管理经验？随着互联网技术的发展，能被</span>“标签化”的技能越来越多后，零工经济繁荣背后是大势所趋还是临时手段？极度灵活和极度稳定的利弊各是什么？
	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
		<span>员工多了企业第一反应是优化，而不是</span>“共享员工”
	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
		<span>企业的发展问题在疫情里凸显，包括用人规模和方式。</span>
	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
		“在正常的生产经营里，大部分企业都处于招人状态，一旦员工多了，第一个反应是做优化，而不是借调到别的企业。”陈鸿飞告诉投中网，此次疫情有特殊场景，涉及到企业能否开工、能否营业，才有员工“富裕过剩”。
	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
		<span>出于职业习惯，陈鸿飞关注的是如何实现</span>“共享员工”。他表示“共享员工”从法律层面上不属于劳务派遣，而是一次外包服务，派出的员工仍和派出方企业仍保持合同关系。“以盒马的为例，可以理解成盒马向云海肴采购了一次服务行为，后者收取的是外包服务费。”陈鸿飞解释说。
	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
		<span>从含义上理解，</span>“共享员工”和原来的服务外包或岗位外包的模式相同。这些原来由人力资源公司或是种第三方平台来提供服务外包的业务，“但因为特殊时期，共享员工由企业双方自身完成了。而且因为有盒马这样知名度的企业宣传出来，所以大家会觉得是新形态。”陈鸿飞说。
	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
