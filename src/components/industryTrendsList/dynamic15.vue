	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
		<span>而这背后，其实也强调了零工经济对人力成本管理的挑战</span>——企业组织调配机制的升级。陈鸿飞表示，这次疫情后，很多企业会重新思考这个问题：如果灵活用工用得少了，会不会很麻烦？
	</p>
