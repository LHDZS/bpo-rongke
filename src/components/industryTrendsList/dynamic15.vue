	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
		<span>他告诉投中网在疫情期间，美菜的家庭业务量急剧增大，但缺人手拣货和派送。彼时，</span>“共享员工”盛行，美菜顺势也加入其中，通过劳务派谴机构向各个城市寻求“闲置”且可通行的健康司机、餐厅员工等服务。
	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
		2月初，盒马发起了“共享员工”，与诸如西贝有2万员工“闲置”在宿舍，形成了鲜明对比。前者利用灵活用工的模式解决人力不足，后者则线下门店有大量长期雇佣关系而在疫情里人力过于富足。
	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
		<span>与传统的长期雇佣员工不同，在新经济中顺势而生的零工经济，利用互联网和移动技术快速匹配供需方，形成了</span>“临时”新工作模式。某种程度上，这种又被称作“聘请承包商”的模式，能为负债经营的科技公司节省许多人力成本：不需要支付社保、福利、甚至股权等。而且，对硅谷的科技企业来说，它还避免了相关机构对招聘、裁员比例的审查。
	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
		<span>以谷歌为例，据《华盛顿邮报》报道，截至</span>2019年3月，它大约有12.1万名临时工、供应商和承包商，而全职员工只有10.2万人。在此次疫情里，包括谷歌在内的硅谷科技公司关闭了办公室后，或会雇佣更多承包商以及临时工——因为他们的医疗保障和福利低于全职员工。
	</p>
	<p class="p" style="text-indent:21pt;vertical-align:baseline;background:#FFFFFF;">
		“疫情就像一次对风险的考验。”元璟资本合伙人田敏对投中网说。企业很多问题在本次疫情里暴露。由“共享员工”引发企业开始思考如何灵活用工，是这次疫情中被讨论最多的话题之一。背后，实际是用人成本以及人力资源的搭配博弈。
