	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>共享员工与社会化用工</span>
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		“共享员工”是灵活用工的一种表现形式。
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>企业尤其是生产制造和线下服务等员工人数众多的企业，在面对业务量的增大或减少时，希望企业员工能够灵活调动，从而实现在淡季缩减人手降低成本，在旺季可以随时增加人手，提高企业的投入产出比。</span>
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>但是由于种种原因，此前企业仅通过第三方人力资源机构的派遣、外包等服务方式实现部分灵活用工。</span>
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>大致模式是员工</span>A与一家人力资源机构签订劳动合同，然后该机构将A派遣到相应的企业上班，比如餐饮门店企业。
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>而此次突发疫情推动了企业为了生存与发展，主动采取</span>“共享员工”方式。
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>那么共享员工与用友薪福社一直以来倡导的社会化用工，是一个概念吗？</span>
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>我们从以下几个维度来看：</span>
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>法律关系不同</span>
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>共享员工方式下，员工个体与企业组织主体，无论通过第三方机构中间调配或者两个组织之间</span>“借调”还是与新公司签合同，仍然存在雇佣关系或事实劳动关系。
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>即此员工还是雇佣于某家人力资源机构或者是附属于某一机构，该机构或者组织对员工个体可以实行强管控，比如上班时间、上班工作任务管控等。</span>
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>而社会化用工，则是组织与个人，在风险共担，利益共享的共识下建立的合作关系，是一种组织与外部个人平等关系下的合作。</span>
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>典型的就是滴滴司机和滴滴平台之间的合作，网红主播与直播平台、电商平台的合作。</span>
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>应用出发点不同</span>
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>共享员工是在疫情突发的特殊时期，需求方企业由于线上业务爆发带来巨额的员工需求量，或此前就面临人手不够的问题，而供给方企业在疫情下背负着沉重的固定人工成本，又无法产生收入，双方一拍即合形成合作；</span>
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>而企业应用社会化用工则是出于促进企业高速发展的目的，在部分职能上，主动设计和应用了组织与个人的新配置模式，实现更高</span>“人效”和快速业绩增长，例如地产商的“全民营销”方式。
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>应用职能不同</span>
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>共享员工的应用岗位，主要是经过简单培训即可快速上岗的职能，例如理货、打包、派送等，是易于标准化流程化的劳动密集型岗位；</span>
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>社会化用工的应用，则是企业对部分岗位职能进行重构和运营模式调整，对其中的工作任务分解和拆分，形成工作任务市场，具备了由组织外部的个人领取和完成任务的可能，这部分工作任务具有一定的专业能力或资源要求，企业通过社会化用工应用外部能力，支持自身目标达成。</span>
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>其中影视剧剧组就非常典型，其中的服装造型、化妆、道具等，都是由具备专项技能和资源的强个体与影视公司形成合作，支持剧组完成拍摄。</span>
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>管理运营模式不同</span>
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>共享员工由于仍然是一种雇佣或事实劳动关系，因此需要纳入到企业的人力资源管理体系，包括招聘、培训、员工关系管理、绩效、薪酬、社保等多项内容，从前面各企业的共享员工的招聘要求也能体现出来，同时组织要承担相对较高的管理成本；</span>
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>社会化用工，则强调的是组织对外部合作者的运营，不适用企业的人力资源管理方式，更多地是从合作对象画像定义、触达、吸引引流、运营（线上、线下）、（短期与中长期）激励、风险控制等维度，实现基于合作者个人以及其行为数据的运营，进而达成企业发展目标，在这一点上，部分互联网金融服务平台对在线营销人员的运营和激励就是很好的例子。</span>
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>比如滴滴平台会根据滴滴司机每日接单数量、接单路线，通过系统给予一定的奖励，同时对违规的接单行为也会进行相应处罚。</span>
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>个体自主性不同</span>
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>雇佣制下的劳动者，需要根据企业岗位要求，完成相关的工作岗位职责，形象地说如同一个运转着的机器上的一个</span>“螺丝钉”；
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>而在社会化用工中，组织与个人是在平等地位基础上形成合作关系，因此合作者个体可以享受充分的自由，什么时间接单、什么地点完成、收入是否符合要求都可以自己选择。</span>
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>在获得收入的同时享受前所未有的自由，个人的自驱力、创造力都将得到更好的发挥。</span>
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>尤其是这次疫情客观推动了</span>“在家办公”的普及和应用，很多人在疫情期间深切体会到了技术的进步已经让集中办公变得不再那么必要。
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>再者，疫情的突发让不少人意识到，自己应该发展一个基于自身能力的</span>“第二职业”或者“第二收入来源”，以备“放假期间，公司没了”这种极端情况的出现。
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		4
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>疫情之后，共享员工模式发展</span>
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>共享员工是在特殊时期，利用灵活用工方式，帮助困难企业分流员工，助力员工需求量较大企业在业务快速增长下补充人力缺口的阶段性方法，同时也是部分企业在大灾面前保障民生为国分忧的行动，体现了企业对社会责任的担当。</span>
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>突发疫情，推动了共享员工的应用，对双边企业和劳动者都是有利的，但是疫情过后，部分线下服务行业将迎来业务的报复性增长，而到那时的劳动力流动与调配，也许又是一个新的课题。</span>
	</p>
	<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:0.0000pt;text-align:justify;">
		<span>与此同时，逆境中前行的企业，度过这次生死劫后，也需要在业务模式和用工配置方式上提前做出思考和规划。</span>
	</p>
