      <p class="p" style="text-indent:0pt;background:#FFFFFF;">
	<span>要点一、政策施行地</span>
</p>
<p class="p" style="text-indent:0pt;background:#FFFFFF;">
	<span>&emsp;&emsp;该政策出自《</span><a href="https://www.shui5.cn/article/29/138725.html"><span>国务院关于深化北京市新一轮服务业扩大开放综合试点建设国家服务业扩大开放综合示范区工作方案的批复</span></a><span>》</span><span>(</span><a href="https://www.shui5.cn/article/29/138725.html"><span>国函〔</span><span>2020〕123号</span></a><span>)附件《深化北京市新一轮服务业扩大开放综合试点建设国家服务业扩大开放综合示范区工作方案》三(三)18(以下简称“《附件》)。</span>
</p>
<p class="p" style="text-indent:0pt;background:#FFFFFF;">
	<span>&emsp;&emsp;从标题上，我们一眼就可以看出，该政策适用地区为在京企业。所以，不是北京市的企业可以直接忽略这条。</span>
</p>
<p class="p" style="text-indent:0pt;background:#FFFFFF;">
	<span>&emsp;&emsp;要点二、技术领域和主要产品</span>
</p>
<p class="p" style="text-indent:0pt;background:#FFFFFF;">
	<span>&emsp;&emsp;高企认定有关技术领域规定，出自《高新技术企业认定办法》</span><span>(以下简称“《认定办法》”)第十一条第三项，对企业主要产品(服务)发挥核心支持作用的技术属于《国家重点支持的高新技术领域》(以下简称“《领域》”)规定的范围。涉及领域为电子信息、生物与新医药、航空航天、新材料、高技术服务、新能源与节能、资源与环境、先进制造与自动化等八大领域。</span>
</p>
<p class="p" style="text-indent:0pt;background:#FFFFFF;">
	<span>&emsp;&emsp;对应到该政策就是，企业主要产品</span><span>(服务)发挥核心支持作用的技术属于从事集成电路、人工智能、生物医药、关键材料等领域规定的范围。有关主要产品、高新技术产品和销售收入关系是：</span>
</p>
<p class="p" style="text-indent:0pt;background:#FFFFFF;">
