</p>
<p class="p" style="text-indent:0pt;background:#FFFFFF;">
	<span>&emsp;&emsp;要点三、企业年限规定</span>
</p>
<p class="p" style="text-indent:0pt;background:#FFFFFF;">
	<span>&emsp;&emsp;该政策要求满足从业一年以上。有观点认为，</span><span>“报备”时满足365天即可——这个完全没有依据。</span>
</p>
<p class="p" style="text-indent:0pt;background:#FFFFFF;">
	<span>&emsp;&emsp;《认定办法》对认定时间性分为两种类型，一类是含申报年，另外一类不含申报年。</span><span>“申请认定前一年内”是请前的365天之内(含申报年)，《认定办法》第十一条第一项的“申请认定时须注册成立一年以上”是指企业须注册成立365个日历天数以上。</span>
</p>
<p class="p" style="text-indent:0pt;background:#FFFFFF;">
